package b21_2;

public class Q2PairOfNums {

    private int num1;
    private int num2;

    public Q2PairOfNums(int num1, int num2) {
        this.num1 = num1;
        this.num2 = num2;
    }

    public boolean endStart() {
        return true;
    }

    public int getNum1() {
        return num1;
    }

    public void setNum1(int num1) {
        this.num1 = num1;
    }

    public int getNum2() {
        return num2;
    }

    public void setNum2(int num2) {
        this.num2 = num2;
    }
}
